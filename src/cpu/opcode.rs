    pub static ref OPCODES_MAP: HashMap<u8, &'static OpCode> = {
        let mut map = HashMap::new();
        for cpu_op in &*CPU_OPS_CODES {
            map.insert(cpu_op.code, cpu_op);
        }
        map
    };
